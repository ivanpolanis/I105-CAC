HAND 	EQU 40H

		ORG 1000H
CAR 	DB 	?

		ORG 2000H
		IN 	AL,		HAND+1
		AND AL, 	07FH
		OUT HAND+1, AL
		MOV BX, 	OFFSET CAR
		MOV CL, 	0
LEER: 	INT 6
		INC BX 
		INC CL
		CMP CL, 	5
		JNZ LEER
		MOV BX, 	OFFSET CAR
PRINT1: IN 	AL, 	HAND+1
		AND AL, 	1
		JNZ PRINT1
		MOV AL, 	[BX]
		OUT HAND, 	AL
		INC BX
		DEC CL
		JNZ PRINT1
		MOV BX, 	OFFSET CAR + 4
		MOV CL, 	5 
PRINT2: IN AL, 		HAND+1
		AND AL, 	1
		JNZ PRINT2
		MOV AL, 	[BX]
		OUT HAND, 	AL
		DEC BX
		DEC CL
		JNZ PRINT2
		HLT
		END